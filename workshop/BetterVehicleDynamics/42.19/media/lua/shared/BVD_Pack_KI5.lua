local function targetScriptPresent()
    if not ScriptManager then return false end
    local sm = ScriptManager.instance
    if not (sm and sm.getVehicle) then return false end
    for fullType in pairs(DATA) do
        local ok, vs = pcall(function() return sm:getVehicle(fullType) end)
        if ok and vs ~= nil then return true end
    end
    return false
end

local function registerKI5Pack()
    if not (BVD and BVD.registerPack) then
        print("[BVD KI5 Pack] BVD.registerPack unavailable - aborting")
        return
    end
    local known = (BVD.getRegisteredVehicles and BVD.getRegisteredVehicles()) or {}
    local pack, kept = {}, 0
    for fullType, spec in pairs(DATA) do
        if known[fullType] == nil then
            pack[fullType] = spec
            kept = kept + 1
        end
    end
    if kept == 0 then return end
    BVD.registerPack("BVD KI5 Reference", pack, {
        check         = targetScriptPresent,
        priority      = 5,
        source        = "bvd-bundled-ki5 (KI5 collection 2490220997, scan 2026-05-29)",
        -- KI5 cars ship pre-tuned with high engineForce values; this pack
        -- carries real-world reference figures that should overwrite KI5's
        -- baseline, NOT be floored by it.
        authoritative = true,
    })
end

